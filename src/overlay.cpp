// ─── Drawing primitives ─────────────────────────────────────────────────────

void Overlay::fillRect(int x, int y, int w, int h, unsigned bgColor, unsigned char alpha) {
  const unsigned br = (bgColor >> 16) & 0xFF;
  const unsigned bg = (bgColor >> 8) & 0xFF;
  const unsigned bb = bgColor & 0xFF;
  const unsigned inv = 255 - alpha;

  const int x1 = std::max(x, 0);
  const int y1 = std::max(y, 0);
  const int x2 = std::min(x + w, (int)W);
  const int y2 = std::min(y + h, (int)H);

  for (int py = y1; py < y2; ++py) {
    for (int px = x1; px < x2; ++px) {
      unsigned& p = pixels[py * W + px];
      const unsigned sr = (p >> 16) & 0xFF;
      const unsigned sg = (p >> 8) & 0xFF;
      const unsigned sb = p & 0xFF;
      const unsigned r = (br * alpha + sr * inv) / 255;
      const unsigned g = (bg * alpha + sg * inv) / 255;
      const unsigned b = (bb * alpha + sb * inv) / 255;
      p = (r << 16) | (g << 8) | b;
    }
  }
}

void Overlay::drawRect(int x, int y, int w, int h, unsigned color) {
  const int x2 = x + w - 1;
  const int y2 = y + h - 1;
  for (int px = x; px <= x2; ++px) {
    if (px >= 0 && px < (int)W) {
      if (y  >= 0 && y  < (int)H) pixels[y  * W + px] = color;
      if (y2 >= 0 && y2 < (int)H) pixels[y2 * W + px] = color;
    }
  }
  for (int py = y + 1; py < y2; ++py) {
    if (py >= 0 && py < (int)H) {
      if (x  >= 0 && x  < (int)W) pixels[py * W + x ] = color;
      if (x2 >= 0 && x2 < (int)W) pixels[py * W + x2] = color;
    }
  }
}

void Overlay::drawGlyph(int px, int py, unsigned char c, unsigned color, int scale) {
  if (c < 0x20 || c > 0x7E) return;
  const unsigned char* glyph = font[c - 0x20];
  for (int row = 0; row < 8; ++row) {
    unsigned char bits = glyph[row];
    for (int col = 0; col < 8; ++col) {
      if (bits & 1) {
        // Fill a scale x scale block for each lit pixel
        for (int sy = 0; sy < scale; ++sy) {
          const int screenY = py + row * scale + sy;
          if (screenY < 0 || screenY >= (int)H) continue;
          for (int sx = 0; sx < scale; ++sx) {
            const int screenX = px + col * scale + sx;
            if (screenX >= 0 && screenX < (int)W) {
              pixels[screenY * W + screenX] = color;
            }
          }
        }
      }
      bits >>= 1;
    }
  }
}

void Overlay::drawText(int x, int y, const std::string& text, unsigned color, int scale) {
  int cx = x;
  for (char c : text) {
    drawGlyph(cx, y, static_cast<unsigned char>(c), color, scale);
    cx += 8 * scale;
  }
}
